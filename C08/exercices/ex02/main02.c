#include <stdio.h>
#include "ft_abs.h"

int	main(void)
{
	int a;

	a = 42;
	printf("ABS(%d) = %d\n", a, ABS(a));

	a = -42;
	printf("ABS(%d) = %d\n", a, ABS(a));

	a = 0;
	printf("ABS(%d) = %d\n", a, ABS(a));
	
	return (0);
}    	   
     	   
     	   
